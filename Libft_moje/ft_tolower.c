#include "libft.h"

int
	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		c += 32;
	return (c);
}

// The tolower function in C converts an uppercase
// letter to its lowercase equivalent.
// If the input character is already lowercase
// or not a letter, it remains unchanged.